def _check_uniqueness(attrs: list[dict[str, Any]]) -> None:
    """attribute_name の重複を検出"""
    seen: dict[str, str] = {}
    for a in attrs:
        name = a["attribute_name"]
        cat = a["attribute_category"]
        if name in seen:
            raise ValueError(f"attribute_name 重複: {name} (categories: {seen[name]}, {cat})")
        seen[name] = cat


def _check_compat_refs(attrs: list[dict[str, Any]]) -> None:
    """compatible_archetypes と conflicts_with の参照先 attribute_name が ATTRIBUTES 内に存在することを確認"""
    known = {a["attribute_name"] for a in attrs}
    for a in attrs:
        for ref in a.get("compatible_archetypes", []) or []:
            if ref not in known:
                raise ValueError(
                    f"compatible_archetypes 参照エラー: {a['attribute_name']} -> "
                    f"'{ref}' (ATTRIBUTES 内に存在しない)"
                )
        for ref in a.get("conflicts_with", []) or []:
            if ref not in known:
                raise ValueError(
                    f"conflicts_with 参照エラー: {a['attribute_name']} -> "
                    f"'{ref}' (ATTRIBUTES 内に存在しない)"
                )


def _check_category_counts(attrs: list[dict[str, Any]]) -> None:
    """v1.0+ 完結の数量制約 (personality=17, speech=16, archetype=9, visual=5, hobby=5)"""
    counts: dict[str, int] = {
        "personality": 0, "speech": 0, "archetype": 0, "visual": 0, "hobby": 0
    }
    for a in attrs:
        cat = a["attribute_category"]
        if cat not in counts:
            raise ValueError(f"未対応の attribute_category: {cat} (v1.0+ では 5 種のみ)")
        counts[cat] += 1
    expected = {"personality": 17, "speech": 16, "archetype": 9, "visual": 5, "hobby": 5}
    for cat, n in expected.items():
        if counts[cat] != n:
            raise ValueError(
                f"v1.0+ 数量制約違反: {cat} = {counts[cat]} (期待値 {n})"
            )


def render_yaml(attr: dict[str, Any]) -> str:
    """属性 dict を YAML 文字列にレンダリング (キー順固定)"""
    key_order = [
        "attribute_category",
        "attribute_name",
        "display_name_ja",
        "display_name_en",
        "weight_dimension",
        "typical_value_range",
        "description_ja",
        "description_en",
        "examples",
        "compatible_archetypes",
        "has_catchphrase",
        "variant",
        "tags",
        "conflicts_with",
        "core_traits",
        "speech_style",
        "second_person",
        "sentence_endings",
        "catchphrases",
        "tone",
        "notes",
    ]
    ordered: dict[str, Any] = {}
    for k in key_order:
        if k in attr:
            ordered[k] = attr[k]
    # 残りのキー (将来拡張用)
    for k in attr:
        if k not in ordered:
            ordered[k] = attr[k]

    return yaml.dump(
        ordered,
        allow_unicode=True,
        sort_keys=False,
        default_flow_style=False,
        width=1000,
    )


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="ファイル書き込みせず生成予定のパスのみ表示",
    )
    args = parser.parse_args()

    _check_uniqueness(ATTRIBUTES)
    _check_category_counts(ATTRIBUTES)
    _check_compat_refs(ATTRIBUTES)

    written: list[Path] = []
    for attr in ATTRIBUTES:
        rel_dir = ATTRS_ROOT / attr["attribute_category"]
        out = rel_dir / f"{attr['attribute_name']}.yaml"
        if args.dry_run:
            print(f"[dry-run] {out.relative_to(REPO_ROOT)}")
            continue
        rel_dir.mkdir(parents=True, exist_ok=True)
        out.write_text(render_yaml(attr), encoding="utf-8")
        written.append(out)

    if not args.dry_run:
        print(f"✅ {len(written)} 属性 YAML を生成しました:")
        for p in written:
            print(f"   - {p.relative_to(REPO_ROOT)}")
    else:
        print(f"\n[dry-run] {len(ATTRIBUTES)} 属性の予定パスを表示しました")
    return 0


if __name__ == "__main__":
    sys.exit(main())
